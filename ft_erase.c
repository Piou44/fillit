#include "fillit.h"

void		ft_erase(char **map, char c, int cote)
{
	int		x;
	int		y;

	y = 0;
	while (y < cote)
	{
		x = 0;
		while (x < cote)
		{
			if (map[y][x] == c)
				map[y][x] = '.';
			x++;
		}
		y++;
	}
}
